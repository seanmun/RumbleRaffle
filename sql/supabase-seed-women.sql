  e.name,
  e.year,
  e.event_type,
  e.status,
  COUNT(ge.id) as entrant_count
FROM public.events e
LEFT JOIN public.global_entrants ge ON ge.event_id = e.id
GROUP BY e.id, e.name, e.year, e.event_type, e.status
ORDER BY e.event_type, e.year;
